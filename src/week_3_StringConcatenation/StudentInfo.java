package week_3_StringConcatenation;

public class StudentInfo {

    public static void main(String[] args) {
         String student_name = "Tamara";
         byte age = 25;
         char gender = 'F';
         String schoolName = "Cydeo";
         String phoneNumber1 = "(123)-456-7891";
        String phoneNumber2 = "(124)-454-7891";
         boolean isFullTime = true;
         double GPA = 3.5;
        //soutv
        System.out.println("student_name = " + student_name);
        System.out.println("age = " + age);
        System.out.println("gender = " + gender);
        System.out.println("schoolName = " + schoolName);
        System.out.println("phoneNumber = " + phoneNumber1);
        System.out.println("phoneNumber2 = " + phoneNumber2);
        System.out.println("GPA = " + GPA);
        System.out.println("isFullTime = " + isFullTime);

        System.out.println(GPA+10);

    }

}

